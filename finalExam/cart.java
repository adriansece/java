package finalExam;

public class cart extends shop{
    private int id;
    private String productName;
    private double price;
    private int stock;
    private int quantity;

    public cart(int id, String productName, int stock, int quantity)
    {
        this.id = id;
        this.productName = productName;
        this.price = price;
        this.stock = stock;
        this.quantity = quantity;
    }

}
